func getBlockLetter(ch byte) []string {
	// Convert lowercase to uppercase
	if ch >= 'a' && ch <= 'z' {
		ch = ch - 32
	}
	if lines, ok := blockAlpha[ch]; ok {
		return lines
	}
	return nil
}

func getRandomArt() string {
	return asciiArt[rand.Intn(len(asciiArt))]
}
